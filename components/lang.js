function setLanguage(lang) {
  document.documentElement.lang = lang;

  document.querySelectorAll('[data-i18n-key]').forEach(el => {
    const key = el.getAttribute('data-i18n-key');
    if (translations[lang] && translations[lang][key]) {
      if (el.tagName.toLowerCase() === 'ul') {
        el.innerHTML = translations[lang][key];
      } else {
        el.textContent = translations[lang][key];
      }
    }
  });
}

setLanguage('ru');

document.querySelectorAll('.header__lang-link').forEach(link => {
  link.addEventListener('click', e => {
    e.preventDefault();
    const lang = e.target.textContent.toLowerCase();
    setLanguage(lang);

    document.querySelectorAll('.header__lang-link').forEach(l => l.classList.remove('header__lang-link_active'));
    e.target.classList.add('header__lang-link_active');
  });
});